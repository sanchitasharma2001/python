fruits = ("apple", "banana", "cherry")
print(fruits[0])                                  #to print the first item in the fruits tuple.
print(len(fruits))                                #to print the number of items in the fruits tuple.
print(fruits[-1])                                 #negative indexing to print the last item in the tuple.
print(fruits[2:5])                                #range of indexes to print the third, fourth, and fifth item in the tuple.
